	public static JTabbedPane geTabbedPane(final KLinePainter kLinePainter, final KLineType type) {
		final JTabbedPane tabbedPane = new JTabbedPane();
		tabbedPane.addChangeListener(new ChangeListener() {

			@Override
			public void stateChanged(ChangeEvent e) {
				String title = tabbedPane.getTitleAt(tabbedPane.getSelectedIndex());
				int index = tabbedPane.getSelectedIndex();
				switch (title) {
				case "base":
					tabbedPane.setComponentAt(index,
							KLineFactory.addStatisticalIndicators(kLinePainter, null, StatisticalIndicators.BASE));
					break;

				case "macd":
					tabbedPane.setComponentAt(index, KLineFactory.addStatisticalIndicators(kLinePainter,
							getMacdVos(type), StatisticalIndicators.MACD));
					break;

				case "kdj":
					tabbedPane.setComponentAt(index, KLineFactory.addStatisticalIndicators(kLinePainter,
							getKDJVOS(type), StatisticalIndicators.KDJ));
					break;

				case "atr":
					tabbedPane.setComponentAt(index, KLineFactory.addStatisticalIndicators(kLinePainter, getSSVO(type),
							StatisticalIndicators.ATR));
					break;

				case "boll":
					tabbedPane.setComponentAt(index, KLineFactory.addStatisticalIndicators(kLinePainter,
							getBollVos(type), StatisticalIndicators.BOLL));
					break;
				}
			}
		});
		tabbedPane.add("base", KLineFactory.addStatisticalIndicators(kLinePainter, null, StatisticalIndicators.BASE));
		tabbedPane.add("macd", null);
		tabbedPane.add("kdj", null);
		tabbedPane.add("atr", null);
		tabbedPane.add("boll", null);
		return tabbedPane;
	}

